public class Count1 {
	public static void main(String[] args) {
		
		//variable declaration
		int num = 50;
		
		//while loop checks for when num becomes greater than or equal to 100
		while (num <= 100){
			System.out.println(num);
			num ++;
		}//ends while loop
	}
}